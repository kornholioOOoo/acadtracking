    private void DisableaccMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DisableaccMouseClicked
        int row = users.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select an account to disable.");
            return;
        }

        int aId = Integer.parseInt(users.getValueAt(row, 0).toString());
        String currentStatus = users.getValueAt(row, 5).toString();

        if ("Disabled".equalsIgnoreCase(currentStatus)) {
            JOptionPane.showMessageDialog(this, "Account already disabled.");
            return;
        }

        int confirm = JOptionPane.showConfirmDialog(
                this,
                "Are you sure you want to disable this account?",
                "Confirm Disable",
                JOptionPane.YES_NO_OPTION
        );
        if (confirm != JOptionPane.YES_OPTION) {
            return;
        }

        config cn = new config();
        String sql = "UPDATE tbl_accounts SET status = 'Disabled' WHERE a_id = ?";
        cn.addRecord(sql, aId);

        JOptionPane.showMessageDialog(this, "Account disabled successfully.");
        displayUsers();
    }//GEN-LAST:event_DisableaccMouseClicked


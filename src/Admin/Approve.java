    private void ApproveaccMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_ApproveaccMouseClicked
        int row = users.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Please select an account to approve.");
            return;
        }

        int aId = Integer.parseInt(users.getValueAt(row, 0).toString());
        String status = users.getValueAt(row, 5).toString();

        if ("Active".equalsIgnoreCase(status)) {
            JOptionPane.showMessageDialog(this, "Account already active.");
            return;
        }

        config cn = new config();
        String sql = "UPDATE tbl_accounts SET status = 'Active' WHERE a_id = ?";
        cn.addRecord(sql, aId);

        JOptionPane.showMessageDialog(this, "Account approved successfully.");
        displayUsers();
    }//GEN-LAST:event_ApproveaccMouseClicked

